package br.gov.caixa.siiac.exception;

/**
 * SIIACException
 */
public class SIIACException extends Exception {
	
	
	private static final long serialVersionUID = -6665772176998169568L;
	
	/**
	 * SIIACException
	 * @param msg
	 */
	public SIIACException(String msg) {
		super(msg);
	}

	/**
	 * SIIACException
	 * @param e
	 * @param msg
	 */
	public SIIACException(Throwable e,String msg) {
		super(msg,e);
	}
	
}
